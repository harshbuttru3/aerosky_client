const mongoose = require('mongoose')
		
const solarProjectSchema =  new mongoose.Schema({
	name:{
		type: String,
		required: true,
		max:40,
	},
	referenceImage:{
		type:String,
		default:'https://img.freepik.com/free-photo/photovoltaic-solar-power-panel-field-green-clean-alternative-power-energy-concept-ai-generative_123827-23424.jpg?size=626&ext=jpg&ga=GA1.1.1369675164.1715472000&semt=sph'
	},
	projectArea:{
		type:String,
		required:true,
	},
	startDate:{
		type:String,
		required:true
	},
	deadline:{
		type:String,
		default:''
	},
	scope:{
		type:String,
		required:true
	},
	industry:{
		type:String,
		default:'Solar'
	},
	type:{
		type:String,
		required:true
	},
	coordinates:{
		type:Object,
		default:{}
	},
	siteData:{
		type:Object,
		default:[]
	},
	annotations:{
		type:Object,
		default:{}
	},
	issues:{
		type:Array,
		default:[]
	},
	deliverablesRequired:{
		type:Array,
		default:[]
	},
	attachments:{
		type:Array,
		default:[]
	},
	status:{
		type:String,
		default:'Ongoing project'
	},
	comments:{
		type:Array,
		default:[]
	},
	approvedBy:{
		type:Object,
		default:{}
	},
	progress:{
		type:Number,
		default:0
	},
	approvedAt:{
		type:String,
		default:''
	},
	teamMembers:{
		type:Array,
		default:[]
	},
	clientDetails:{
		type:Object,
		required:true
	},
	pilots:{
		type:Array,
		default:[]
	},
	processingTeam:{
		type:Array,
		default:[]
	},
	groups:{
		type:Array,
		default:[]
	},
	missions:{
		type:Array,
		default:[]
	},
	folders:{
		type:Array,
		default:[]
	},
	thermalFolders:{
		type:Array,
		default:[]
	},
	ongoingMissions:{
		type:Array,
		default:[]
	},
	completedMissions:{
		type:Array,
		default:[]
	}
	},
	{
		timestamps:true,
	}
)


module.exports = mongoose.model("SolarProject",solarProjectSchema)

